package bean;



public class Coupon {

	private String COUPON_ID;

	private String COUPON_TITLE;

	private String COUPON_TEXT;

	public String getCOUPON_ID() {
		return COUPON_ID;
	}

	public void setCOUPON_ID(String couponId) {
		COUPON_ID = couponId;
	}

	public String getCOUPON_TITLE() {
		return COUPON_TITLE;
	}

	public void setCOUPON_TITLE(String couponTitle) {
		COUPON_TITLE = couponTitle;
	}

	public String getCOUPON_TEXT() {
		return COUPON_TEXT;
	}

	public void setCOUPON_TEXT(String couponText) {
		COUPON_TEXT = couponText;
	}
}